export type Lang = 'th' | 'en';

const STORE_KEY = 'memo.lang';

export function getStoredLang(): Lang {
  if (typeof localStorage !== 'undefined') {
    const v = localStorage.getItem(STORE_KEY);
    if (v === 'th' || v === 'en') return v;
  }
  return 'th';
}

